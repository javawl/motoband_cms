package com.motoband.rolemanage;


public class rolelist {
	private String role_guid;
	private String role_name;
    private String role_des;

	public String getRole_guid() {
		return role_guid;
	}

	public void setRole_guid(String role_guid) {
		this.role_guid = role_guid;
	}

	public String getRole_name() {
		return role_name;
	}

	public void setRole_name(String role_name) {
		this.role_name = role_name;
	}

	public String getRole_des() {
		return role_des;
	}

	public void setRole_des(String role_des) {
		this.role_des = role_des;
	}



}
