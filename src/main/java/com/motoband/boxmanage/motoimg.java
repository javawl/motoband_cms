package com.motoband.boxmanage;

public class motoimg {
	private String img_guid;
	private String img_url;
	private String img_opurl;
	private long img_time;
	private String img_writerguid;
	private String img_name;
	private String img_groupid;


	public String getImg_guid() {
		return img_guid;
	}

	public void setImg_guid(String img_guid) {
		this.img_guid = img_guid;
	}

	public String getImg_url() {
		return img_url;
	}

	public void setImg_url(String img_url) {
		this.img_url = img_url;
	}

	public String getImg_opurl() {
		return img_opurl;
	}

	public void setImg_opurl(String img_opurl) {
		this.img_opurl = img_opurl;
	}

	public long getImg_time() {
		return img_time;
	}

	public void setImg_time(long img_time) {
		this.img_time = img_time;
	}

	public String getImg_writerguid() {
		return img_writerguid;
	}

	public void setImg_writerguid(String img_writerguid) {
		this.img_writerguid = img_writerguid;
	}

	public String getImg_name() {
		return img_name;
	}

	public void setImg_name(String img_name) {
		this.img_name = img_name;
	}

	public String getImg_groupid() {
		return img_groupid;
	}

	public void setImg_groupid(String img_groupid) {
		this.img_groupid = img_groupid;
	}



}
