package com.motoband.boxmanage;

public class motoimgbox {
	private String ib_guid;
	private String img_guid;
	private String box_id;

	public String getIb_guid() {
		return ib_guid;
	}

	public void setIb_guid(String ib_guid) {
		this.ib_guid = ib_guid;
	}

	public String getImg_guid() {
		return img_guid;
	}

	public void setImg_guid(String img_guid) {
		this.img_guid = img_guid;
	}

	public String getBox_id() {
		return box_id;
	}

	public void setBox_id(String box_id) {
		this.box_id = box_id;
	}

}
