package com.motoband.boxmanage;

public class BoxBannerModel {
	private long boxbannerid;
	private long score;
	private String title;
	private int type;
	private String subtitle;
	private String url;
	private int boxid;
	private String imgurl;
	private int state;

	


	public long getBoxbannerid() {
		return boxbannerid;
	}

	public void setBoxbannerid(long boxbannerid) {
		this.boxbannerid = boxbannerid;
	}

	public long getScore() {
		return score;
	}

	public void setScore(long score) {
		this.score = score;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public String getSubtitle() {
		return subtitle;
	}

	public void setSubtitle(String subtitle) {
		this.subtitle = subtitle;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public int getBoxid() {
		return boxid;
	}

	public void setBoxid(int boxid) {
		this.boxid = boxid;
	}

	public String getImgurl() {
		return imgurl;
	}

	public void setImgurl(String imgurl) {
		this.imgurl = imgurl;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

}
